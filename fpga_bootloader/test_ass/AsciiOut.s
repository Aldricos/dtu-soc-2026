.section .text
.globl _start

_start:
    # Cache base
    lui   t0, 0xe0000        # t0 = 0xe0000000

    # VGA character memory base
    lui   t1, 0xf0020        # t1 = 0xf0020000

    # ------------------------------------------------------------
    # Character 0: 'H' = 0x48
    # ------------------------------------------------------------
    lw    t6, 0(t0)          # fill cache line
    li    t2, 0x48
    sw    t2, 0(t0)
    lw    t3, 0(t0)          # read back through cache
    sb    t3, 0(t1)          # write to VGA char 0

    # ------------------------------------------------------------
    # Character 1: 'I' = 0x49
    # ------------------------------------------------------------
    lw    t6, 4(t0)
    li    t2, 0x49
    sw    t2, 4(t0)
    lw    t3, 4(t0)
    sb    t3, 1(t1)

    # ------------------------------------------------------------
    # Character 2: 'T' = 0x54
    # ------------------------------------------------------------
    lw    t6, 8(t0)
    li    t2, 0x54
    sw    t2, 8(t0)
    lw    t3, 8(t0)
    sb    t3, 2(t1)

    # ------------------------------------------------------------
    # Character 3: '!' = 0x21
    # ------------------------------------------------------------
    lw    t6, 12(t0)
    li    t2, 0x21
    sw    t2, 12(t0)
    lw    t3, 12(t0)
    sb    t3, 3(t1)

done:
    j     done